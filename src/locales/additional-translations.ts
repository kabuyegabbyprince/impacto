export function useLocalTranslation() {
  const { t: baseT, i18n } = useTranslation();
  const lang = (i18n.language || "en").substring(0, 2);
  const dict = translations[lang] || translations.en;

  return {
    t: (key: string, o?: Record<string, string | number> | string) => {
      if (dict[key] || translations.en[key]) {
        let val = dict[key] || translations.en[key];
        if (o && typeof o === "object") {
          Object.keys(o).forEach((k) => {
            val = val.replace(`{${k}}`, String(o[k]));
          });
        }
        return val;
      }
      if (typeof o === "string") {
        return baseT(key, o);
      }
      return baseT(key, o);
    },
    lang
  };
}
